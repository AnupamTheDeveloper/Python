#Compute a)5 to the power of 8 b)square root of 400 
# c)exponent of 5 d)Logarithm of 625 base 5
import math
part_a=5**8
print(f"5 to the power of 8: {part_a}") #part a complete

part_b=math.sqrt(400)
print(f"square root of 400: {part_b}") #part b complete

part_c=math.exp(5)
print(f"exponent of 5: {part_c}") #part c complete

part_d=math.log(625,5)
print(f"Logarithm of 625 base 5: {part_d}")